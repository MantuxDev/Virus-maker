    private void btnAvantiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAvantiActionPerformed
        // TODO add your handling code here:
        getEvent();
        
        if(rbtnMessaggio.isSelected()){
            Messaggio frame =new Messaggio(evento);
            frame.show();
            frame.setLocationRelativeTo(null);
            evento.setRadio(0);
            evento.setAzione(listaAzioni.getSelectedIndex());
            this.dispose();
        }
        else if(rbtnEvento.isSelected()){
            UnclosableWindow frame=new UnclosableWindow();
            frame.show();
            frame.setLocationRelativeTo(null);
            evento.setRadio(1);
            evento.setAzione(listaAzioni.getSelectedIndex());
            frame.aggiornaModel();
            this.dispose();
        }
    }//GEN-LAST:event_btnAvantiActionPerformed

