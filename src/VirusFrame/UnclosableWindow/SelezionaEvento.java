    private void cambioLingua(){
        
        messaggi.clear();
        eventi.clear();
        
        if(c=='0'){
            labelSelezionaAzione.setText("Seleziona azione:");
            rbtnMessaggio.setText("Messaggio");
            rbtnEvento.setText("Evento");
            labelScegliAzione.setText("Scegli azione:");
            btnAvanti.setText("Avanti");
            
            //Array
            messaggi.add("Errore");
            messaggi.add("Avviso");
            messaggi.add("Informazione");
            
            updateModel(messaggi,modelMesaggi);
            
            eventi.add("Spegni pc");
            eventi.add("Riavvia pc");
            eventi.add("Disconetti utente");
            eventi.add("Blocca pc");
            
            updateModel(eventi,modelEventi);
            
            // Titolo
            this.setTitle("Aggiungi azione");
        }
        else if(c=='1'){
            labelSelezionaAzione.setText("Select action:");
            rbtnMessaggio.setText("Message");
            rbtnEvento.setText("Event");
            labelScegliAzione.setText("Choose action:");
            btnAvanti.setText("Next");
            
            //Array
            messaggi.add("Error");
            messaggi.add("Attention");
            messaggi.add("Information");
            
            updateModel(messaggi,modelMesaggi);
            
            eventi.add("Turn off the pc");
            eventi.add("Restart pc");
            eventi.add("User logout");
            eventi.add("Lock pc");
            
            updateModel(eventi,modelEventi);
            
            // Titolo
            this.setTitle("Add action");
        }
    }
    
    private void updateModel(ArrayList v,DefaultListModel model){
        model.addAll(v);
    }
    
    private void getEvent(){
        int index=listaAzioni.getSelectedIndex();
        int radio;
        
        if(index==-1){
            
            if(c=='0'){
                JOptionPane.showMessageDialog(null, "Seleziona prima un elemento della lista", "Errore",JOptionPane.WARNING_MESSAGE);
                return;
            }
            else if(c=='1'){
                JOptionPane.showMessageDialog(null, "Select an item from the list first", "Error",JOptionPane.WARNING_MESSAGE);
                return;
            }
            return;
        }
        

        if(rbtnMessaggio.isSelected()){
            radio=0;
            evento.setRadio(radio);
            evento.setAzione(index);
        }
        else if(rbtnEvento.isSelected()){
            radio=1;
            evento.setRadio(radio);
            evento.setAzione(index);
        }
    }
    
